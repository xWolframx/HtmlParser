CREATE MEMORY TABLE IF NOT EXISTS site
(
id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1),
url LONGVARCHAR NOT NULL,
domain LONGVARCHAR NOT NULL,
title LONGVARCHAR NOT NULL,
countWords INTEGER NOT NULL
);

CREATE MEMORY TABLE IF NOT EXISTS word
(
id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1),
name VARCHAR(50) NOT NULL,
count INTEGER NOT NULL,
id_site BIGINT NOT NULL,
FOREIGN KEY (id_site) REFERENCES site ON DELETE CASCADE
);

COMMIT;